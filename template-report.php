    <h3>What is National Day?</h3>
    <iframe width="560" height="315" src="//www.youtube.com/embed/cJUHvcDfnhc" frameborder="0" allowfullscreen></iframe>
    <p>National Day is the biggest gathering of civic hackers in the world. It brings technologists, entrepreneurs, developers and other citizens together to improve their communities and the governments that serve them.</p>
    <p>Many communities have existing frameworks for civic hacking events—like Code for America brigades and Random Hacks of Kindness groups—that National Day supports and features on hackforchange.org. Anyone with ideas can participate or organize local events, which include hackathons, unconferences, meetups, block parties and a whole lot more.</p>
    <p>Throughout the weekend (and beyond, in many cases) participants create services, apps and websites that address social and civic problems, like environmental impact, accessibility, transit, homelessness, and civic transparency.</p>
    <p>ND is organized by SecondMuse, Code for America and Innovation Endeavors.</p>
    <h4>The history</h4>
    <p>The first official National Day event occurred in 2012, though civic hacking events and projects—such as Random Hacks of Kindness–have been happening for years. The goal of National Day is to bring all these local events together to provide a framework for collaboration, and a global acknowledgment of the impact that engagement and volunteerism can have in our towns and cities.</p>
    <p>2014 marked the first year that National Day expanded outside of the U.S. There were 30% more events and 24% more cities—making it the largest mass collaboration in history!</p>
